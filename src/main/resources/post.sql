create table address
(
    id     bigint generated by default as identity
        primary key,
    city   varchar(255),
    number integer not null,
    street varchar(255)
);

create table building
(
    id               bigint generated by default as identity
        primary key,
    building_deleted boolean not null,
    name             varchar(255),
    address_id       bigint
        constraint fkf3ryyh4bd143l5b8stt65hwgr
            references address
);

create table floor
(
    id            bigint generated by default as identity
        primary key,
    floor_deleted boolean not null,
    floor_name    varchar(255),
    floor_number  integer not null,
    building_id   bigint
        constraint fkfvb11l7lpgqc6qdrg3bm24kr3
            references building
);

create table room
(
    id           bigint generated by default as identity
        primary key,
    room_deleted boolean not null,
    room_name    varchar(255),
    floor_id     bigint
        constraint fkstlo96g0nkwp4urd4e0ki5b3h
            references floor
);

create table desk
(
    id           bigint generated by default as identity
        primary key,
    desk_deleted boolean not null,
    desk_name    varchar(255),
    is_available boolean not null,
    room_id      bigint
        constraint fk1bdrqq8yryu4slrjw05x8ao8s
            references room
);

create table users_table
(
    user_id          bigint  not null
        primary key,
    active           boolean not null,
    default_floor_id bigint,
    email            varchar(255),
    first_name       varchar(255),
    last_name        varchar(255),
    middle_name      varchar(255),
    password         varchar(255),
    role             integer
);

create table reservation
(
    id                 bigint generated by default as identity
        primary key,
    date               date,
    reservation_status integer,
    desk_id            bigint
        constraint fkoja8a2t4e96cn6kdx8k1t9o3w
            references desk,
    user_user_id       bigint
        constraint fktq5jyp5dqvp3d3oup0yj84cai
            references users_table
);

